package uk.ac.cam.oda22.pathplanning;

import java.awt.geom.Point2D;
import java.util.List;

/**
 * @author Oliver
 *
 */
public class VisibilityChangeList {

	public final List<Point2D> vertices;
	
	/**
	 * The tether points by their distance from the anchor point.
	 */
	public final List<Double> wList;

	/**
	 * The tether points.
	 */
	public final List<Point2D> xList;
	
	public VisibilityChangeList(List<Point2D> vertices, List<Double> wList, List<Point2D> xList) {
		this.vertices = vertices;
		this.wList = wList;
		this.xList = xList;
	}
	
}
